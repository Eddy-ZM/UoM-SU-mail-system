function parseEmail(html) {
  return new DOMParser().parseFromString(html, "text/html");
}

function ensureHiddenCreditInDocument(doc) {
  const walker = doc.createTreeWalker(doc, 128);
  let comment = walker.nextNode();
  while (comment) {
    if ((comment.nodeValue || "").trim() === HIDDEN_CREDIT) return false;
    comment = walker.nextNode();
  }
  doc.documentElement.appendChild(doc.createComment(` ${HIDDEN_CREDIT} `));
  return true;
}

export function ensureHiddenCredit(html) {
  const doc = parseEmail(html);
  if (!ensureHiddenCreditInDocument(doc)) return html;
  return `<!doctype html>\n${doc.documentElement.outerHTML}`;
}

