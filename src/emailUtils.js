function setModuleEnabled(doc, moduleId, enabled) {
  doc.querySelectorAll(`[data-email-module="${moduleId}"]`).forEach((element) => {
    element.dataset.moduleEnabled = enabled ? "true" : "false";
    if (enabled) {
      element.removeAttribute("data-content-block-deleted");
      element.removeAttribute("hidden");
      element.style.removeProperty("display");
      const style = (element.getAttribute("style") || "")
        .replace(/(?:^|;)\s*mso-hide\s*:[^;]*/gi, "")
        .replace(/^\s*;|;\s*$/g, "")
        .trim();
      if (style) element.setAttribute("style", style);
      else element.removeAttribute("style");
      return;
    }

    element.setAttribute("hidden", "");
    element.style.setProperty("display", "none", "important");
    const style = element.getAttribute("style") || "";
    if (!/mso-hide\s*:/i.test(style)) element.setAttribute("style", `${style} mso-hide:all;`.trim());
  });
}

